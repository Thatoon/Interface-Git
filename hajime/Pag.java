    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        double numb3 = Double.parseDouble(txt1.getText()),  numb4 = Double.parseDouble(txt2.getText()), calc = numb3/(numb4*numb4);
        String form = String.format("%.2f", calc);
        Resultado.setText(form);  
        
        if(calc < 18.5){
            ST.setText("Abaixo do Peso");
        }else if (calc > 18.5 && calc < 20){
            ST.setText("Peso Ideal");
        }else if (calc > 20 && calc < 25){
            ST.setText("Acima do Peso");
        }else if (calc > 25 && calc < 30){
            ST.setText("Obesidade +");
        }else{
            ST.setText("Não há resultado");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

